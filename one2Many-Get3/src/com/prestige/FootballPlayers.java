package com.prestige;

public class FootballPlayers {
 
	private Integer fid;
	private String players_name;
	private Integer forevenid;
	public Integer getFid() {
		return fid;
	}
	public void setFid(Integer fid) {
		this.fid = fid;
	}
	public String getPlayers_name() {
		return players_name;
	}
	public void setPlayers_name(String players_name) {
		this.players_name = players_name;
	}
	public Integer getForevenid() {
		return forevenid;
	}
	public void setForevenid(Integer forevenid) {
		this.forevenid = forevenid;
	}
	
}
